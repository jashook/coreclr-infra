////////////////////////////////////////////////////////////////////////////////

public class AzureDevOpsTrackingMain
{
    public async static Task MainAsync()
    {
        var tracker = new AzureDevopsTracking();

        await tracker.Update();
    }

    public static void Main()
    {
        MainAsync().Wait();
    }
}