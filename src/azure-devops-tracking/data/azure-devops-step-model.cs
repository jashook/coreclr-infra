using System;
using System.Collections.Generic;

using DevOps.Util;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

namespace models {

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

public class AzureDevOpsStepModel
{
    public string Id { get; set; }
    public DateTime DateStart { get; set; }

    public DateTime DateEnd { get; set; }

    public double ElapsedTime { get; set; }

    public TaskResult Result { get; set; }

    public string Console { get; set; }

    public string ConsoleUri { get; set; }

    public string Name { get; set; }

    public bool Skipped { get; set; }

    public bool IsHelixSubmission { get; set; }

    public string Machine { get; set; }

    public string StepGuid { get; set; }

    public string ParentGuid { get; set; }

    public override string ToString() => Name;
}

class StepComparer : IComparer<AzureDevOpsStepModel>
{
    public int Compare(AzureDevOpsStepModel lhs, AzureDevOpsStepModel rhs)
    {
        if (lhs.DateStart < rhs.DateStart) return -1;
        else if (lhs.DateStart == rhs.DateStart) return 0;
        else return 1;
    }
}

